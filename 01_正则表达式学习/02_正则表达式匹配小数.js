// js中的小数有几种情况
// 整数
let reg1 = /^([-]{0,1}[1-9][0-9]{0,}|0)$/
// n.
let reg2 = /^([-]{0,1}[1-9][0-9]{0,}|0)\.{0,1}$/
let reg12 = /^([-]{0,1}[1-9][0-9]{0,}|0)\.$/
// n.n
let reg3 = /^([-]{0,1}[1-9][0-9]{0,}|0)\.[0-9]{1,}$/
let reg23 = /^([-]{0,1}[1-9][0-9]{0,}|0)\.[0-9]{0,}$/
let reg123 = /^([-]{0,1}[1-9][0-9]{0,}|0)(\.[0-9]{0,}){0,1}$/
// .n
let reg4 = /^\.[0-9]{1,}$/
// 综上
let reg = /^(([-]{0,1}[1-9][0-9]{0,}|0)(\.[0-9]{0,}){0,1})|\.[0-9]{1,}$/

console.log(reg.test('1'))
console.log(reg.test('1.'))
console.log(reg.test('1.1'))
console.log(reg.test('.1'))
console.log(reg.test('.'))